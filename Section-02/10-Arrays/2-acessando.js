// Neste arquivo, vamos descobrir como acessar os itens de um Array

// 1- Acessando o valor de um index do array
const students = ['Pedro', 'Luane', 'Daniel']
console.log(students[0]); // Pedro
console.log(students[1]); // Luane
console.log(students[2]); // Daniel
// Ele acessa o valor no index do que foi passado no []

// 2- Salvando um valor de um array em uma variavel
const stu1 = students[0] // Pedro
const stu2 = students[1] // Luane
const stu3 = students[2] // Daniel