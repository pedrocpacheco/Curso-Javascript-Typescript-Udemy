/*
Operadores de Comparação:
> maior que
>= maior ou igual 
< menor que
<= menor ou igual
== igualdade (valor) "10" == 10
=== igualdade (valor e tipo) 10 === 10
!= diferente (valor) "10" != 9
!== diferente (valor e tipo) "10" !== 10
*/

const comp = 10 > 5 // boolean
console.log(comp) // true

console.log(10 > 5); // false
console.log(10 >= 10) // true

console.log(10 < 5); // false
console.log(5 <= 5); // true

console.log(10 == "10"); // true
console.log(10 === "10"); // false

console.log(10 != "10"); // false
console.log(10 !== "10"); // true

